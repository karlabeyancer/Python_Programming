"""A Module that contains many function
that handles computation 
"""

def add(a,b):
    """This function adds two numbers together"""
    return a + b

def subtract(a,b):
    """This function subtracts two numbers together"""
    return a - b

def mult(a,b):
    """This function multiplies two numbers together"""
    return a * b

def div(a,b):
    """This function divides two numbers together"""
    return a/b  #Interger division  (/= Decimal division e.g 5/2 = 2.5)

# if __name__ == "__main__":  # This magic method prevents the function to be called from anywhere
def ispow(a,b):
    """This function raises a number to the power of another number"""
    return a**b

 